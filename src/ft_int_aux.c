#include "libft.h"

void	ft_pf_putllint_aux(long long int nb)
{
	if (nb < 0)
	{
		if (nb == (-9223372036854775807 - 1))
		{
			write (1, "9223372036854775808", 19);
			return ;
		}
		else
			nb *= -1;
	}
	if (nb >= 10)
		ft_pf_putllint_aux(nb / 10);
	ft_putchar('0' + nb % 10);
}
